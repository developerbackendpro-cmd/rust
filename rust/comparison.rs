fn main() {

    // 1. Teng (==)
    // 1. Равно (==)
    let a = 10;
    let b = 10;
    let c = a == b;
    println!("1) -► Teng: {} == {} => {}", a, b, c);
    // 1) -► Teng: 10 == 10 => true

    // 2. Teng emas (!=)
    // 2. Не равно (!=)
    let a = 10;
    let b = 5;
    let c = a != b;
    println!("2) -► Teng emas: {} != {} => {}", a, b, c);
    // 2) -► Teng emas: 10 != 5 => true

    // 3. Kichik (<)
    // 3. Меньше (<)
    let a = 5;
    let b = 10;
    let c = a < b;
    println!("3) -► Kichik: {} < {} => {}", a, b, c);
    // 3) -► Kichik: 5 < 10 => true

    // 4. Katta (>)
    // 4. Больше (>)
    let a = 15;
    let b = 10;
    let c = a > b;
    println!("4) -► Katta: {} > {} => {}", a, b, c);
    // 4) -► Katta: 15 > 10 => true

    // 5. Kichik yoki teng (<=)
    // 5. Меньше или равно (<=)
    let a = 10;
    let b = 10;
    let c = a <= b;
    println!("5) -► Kichik yoki teng: {} <= {} => {}", a, b, c);
    // 5) -► Kichik yoki teng: 10 <= 10 => true

    // 6. Katta yoki teng (>=)
    // 6. Больше или равно (>=)
    let a = 10;
    let b = 5;
    let c = a >= b;
    println!("6) -► Katta yoki teng: {} >= {} => {}", a, b, c);
    // 6) -► Katta yoki teng: 10 >= 5 => true

    // 7. Turli tiplarni taqqoslash (cast qilish kerak)
    // 7. Сравнение разных типов (нужно приведение)
    let a: i32 = 10;
    let b: i64 = 10;
    let c = a == b as i32;
    println!("7) -► Turli tiplar: {} == {} => {}", a, b, c);
    // 7) -► Turli tiplar: 10 == 10 => true
}
// #================================================================================================================================================#
// # |  №  | Operator                | Tavsif (UZ)                                          | Описание (RU)                                         |
// #================================================================================================================================================#
// # |   1 | ==                      | Teng                                                 | Равно                                                 |
// # |   2 | !=                      | Teng emas                                            | Не равно                                              |
// # |   3 | <                       | Kichik                                               | Меньше                                                |
// # |   4 | >                       | Katta                                                | Больше                                                |
// # |   5 | <=                      | Kichik yoki teng                                     | Меньше или равно                                      |
// # |   6 | >=                      | Katta yoki teng                                      | Больше или равно                                      |
// # |   7 | as                      | Tiplarni o'zgartirish (cast)                         | Приведение типов (cast)                               |
// #================================================================================================================================================#