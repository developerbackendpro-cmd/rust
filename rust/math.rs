fn main() {

    // 1. Qo'shish (+)
    // 1. Сложение (+)
    let s = 10;
    let a = 5;
    let c = s + a;
    println!("1) -► Qo'shish: 10 + 5 = {}", c);
    // 1) -► Qo'shish: 10 + 5 = 15

    // 2. Ayirish (-)
    // 2. Вычитание (-)
    let s = 20;
    let a = 8;
    let c = s - a;
    println!("2) -► Ayirish: 20 - 8 = {}", c);
    // 2) -► Ayirish: 20 - 8 = 12

    // 3. Ko'paytirish (*)
    // 3. Умножение (*)
    let s = 7;
    let a = 6;
    let c = s *  a;
    println!("3) -► Ko'paytirish: 7 * 6 = {}", c);
    // 3) -► Ko'paytirish: 7 * 6 = 42

    // 4. Bo'lish (/) - butun qism
    // 4. Деление (/) - целая часть
    let s = 25;
    let a = 4;
    let c = s * a;
    println!("4) -► Bo'lish: 25 / 4 = {}", c);
    // 4) -► Bo'lish: 25 / 4 = 6

    // 5. Qoldiq (%)
    // 5. Остаток (%)
    let s = 25;
    let a = 4;
    let c = s % a;
    println!("5) -► Qoldiq: 25 % 4 = {}", c);
    // 5) -► Qoldiq: 25 % 4 = 1

    // 6. Murakkab misol (qavslar bilan)
    // 6. Сложный пример (со скобками)
    let s = 10;
    let a = 5;
    let b = 2;
    let c = 8;
    let g = 2;
    let murakkab = (s + a) * b - c / g;
    println!("6) -► Murakkab: (10 + 5) * 2 - 8 / 2 = {}", murakkab);
    // 6) -► Murakkab: (10 + 5) * 2 - 8 / 2 = 26

    // 7. Turli tiplarni ishlatish (cast qilish kerak)
    // 7. Использование разных типов (нужно приведение)
    let a: i32 = 10;
    let b: i64 = 20;
    let c = a + b as i32;
    println!("7) -► Turli tiplar: {} + {} = {}", a, b, c);
    // 7) -► Turli tiplar: 10 + 20 = 30
}
// #================================================================================================================================================#
// # |  №  | Operator                | Tavsif (UZ)                                          | Описание (RU)                                         |
// #================================================================================================================================================#
// # |   1 | +                       | Qo'shish                                             | Сложение                                              |
// # |   2 | -                       | Ayirish                                              | Вычитание                                             |
// # |   3 | *                       | Ko'paytirish                                         | Умножение                                             |
// # |   4 | /                       | Bo'lish (butun qism)                                 | Деление (целая часть)                                 |
// # |   5 | %                       | Qoldiq (modul)                                       | Остаток (модуль)                                      |
// # |   6 | ( )                     | Qavslar (amallar tartibini belgilaydi)               | Скобки (определяют порядок операций)                  |
// # |   7 | as                      | Tiplarni o'zgartirish (cast)                         | Приведение типов (cast)                               |
// #================================================================================================================================================#