// #================================================================================================================================================#
// #                                                                 RANGES                                                                         #
// #                                       0..5   — 0 dan 4 gacha (5 kirmaydi) / 0..5 — от 0 до 4 (5 не включается)                                 #
// #                                        0..=5  — 0 dan 5 gacha (5 kiradi) / 0..=5  — от 0 до 5 (5 включается)                                   #
// #================================================================================================================================================#

fn main() {

    // 0..5 — exclusive range (5 kirmaydi)
    // исключающий диапазон
    for i in 0..5 {
        print!("{} ", i);
    }
    println!();
    // 0 1 2 3 4

    // 0..=5 — inclusive range (5 kiradi)
    // включающий диапазон
    for i in 0..=5 {
        print!("{} ", i);
    }
    println!();
    // 0 1 2 3 4 5

    // .contains() — qiymat range ichidami?
    // находится ли значение в диапазоне?
    let r = 0..10;
    println!("{}", r.contains(&5));
    println!("{}", r.contains(&10));
    // true
    // false

    // .rev() — teskari aylanish
    // обратный порядок
    for i in (0..5).rev() {
        print!("{} ", i);
    }
    println!();
    // 4 3 2 1 0

    // .step_by() — qadam bilan aylanish
    // с шагом
    for i in (0..10).step_by(2) {
        print!("{} ", i);
    }
    println!();
    // 0 2 4 6 8

    // range dan Vec yaratish
    // создание вектора из диапазона
    let v: Vec<i32> = (1..=5).collect();
    println!("{:?}", v);
    // [1, 2, 3, 4, 5]

    // char range — harflar bo'ylab
    // диапазон символов
    for c in 'a'..='e' {
        print!("{} ", c);
    }
    println!();
    // a b c d e
}
// #================================================================================================================================================#
// # |  №  | Range / Metod           | Tavsif (UZ)                                          | Описание (RU)                                         |
// #================================================================================================================================================#
// # == RANGE TURLARI / ТИПЫ ДИАПАЗОНОВ ============================================================================================================#
// # |   1 | 0..5                    | Exclusive range (oxirgi qiymat kirmaydi)             | Исключающий диапазон (последнее значение не входит)   |
// # |   2 | 0..=5                   | Inclusive range (oxirgi qiymat kiradi)               | Включающий диапазон (последнее значение входит)       |
// # == RANGE METHODLARI / МЕТОДЫ ДИАПАЗОНОВ =======================================================================================================#
// # |   3 | .contains()             | Qiymat range ichida ekanligini tekshiradi            | Проверяет, входит ли значение в диапазон              |
// # |   4 | .rev()                  | Range ni teskari tartibda aylantiradi                | Перебирает диапазон в обратном порядке                |
// # |   5 | .step_by()              | Berilgan qadam bilan aylantiradi                     | Перебирает с указанным шагом                          |
// # |   6 | .collect()              | Range dan vector yaratadi                            | Создаёт вектор из диапазона                           |
// # == CHAR RANGE / ДИАПАЗОН СИМВОЛОВ =============================================================================================================#
// # |   7 | 'a'..='e'               | Harflar bo'ylab aylanish                             | Перебор символов                                      |
// #================================================================================================================================================#